# formula for avg --->  sum of all num/total num

# take num as input from user and convert into int
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
num3 = int(input("Enter the third number: "))

# calculate the avg
avg = (num1 + num2 + num3 )/ 3

# print the avg
print("The average of given number are : ", avg)
